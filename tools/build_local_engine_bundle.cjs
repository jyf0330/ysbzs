#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const outFile = path.join(root, 'web/js/local-engine.js');

function posix(rel) {
  return `/${rel.split(path.sep).join('/')}`;
}

function walk(dir, pred, out = []) {
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, ent.name);
    if (ent.isDirectory()) walk(full, pred, out);
    else if (pred(full)) out.push(full);
  }
  return out;
}

const moduleFiles = [];
const seenModules = new Set();
const REQUIRE_RE = /require\(['"](\.{1,2}\/[^'"]+)['"]\)/g;

function resolveRequiredModule(fromFile, request) {
  const base = path.dirname(fromFile);
  const direct = path.resolve(base, request);
  const candidates = [
    direct,
    `${direct}.cjs`,
    `${direct}.js`,
    path.join(direct, 'index.cjs')
  ];
  return candidates.find(file => fs.existsSync(file) && fs.statSync(file).isFile()) || null;
}

function collectModule(file) {
  const full = path.resolve(file);
  if (seenModules.has(full)) return;
  seenModules.add(full);
  moduleFiles.push(full);
  const code = fs.readFileSync(full, 'utf8');
  for (const match of code.matchAll(REQUIRE_RE)) {
    const child = resolveRequiredModule(full, match[1]);
    if (child && child.startsWith(path.join(root, 'src'))) collectModule(child);
  }
}

collectModule(path.join(root, 'src/adapters/serverAuthorityAdapter.cjs'));
moduleFiles.sort();
const dataFiles = [
  ...walk(path.join(root, 'data/csv'), file => file.endsWith('.csv')).sort(),
  ...walk(path.join(root, 'yaml'), file => /\.ya?ml$/.test(file)).sort()
];

const modules = {};
for (const file of moduleFiles) {
  modules[posix(path.relative(root, file))] = fs.readFileSync(file, 'utf8');
}

const files = {};
for (const file of dataFiles) {
  files[posix(path.relative(root, file))] = fs.readFileSync(file, 'utf8');
}

const bundle = `// Generated by tools/build_local_engine_bundle.cjs. Do not edit by hand.
(function () {
  'use strict';

  const MODULES = ${JSON.stringify(modules)};
  const FILES = ${JSON.stringify(files)};
  const CACHE = Object.create(null);

  function normalizePath(input) {
    const raw = String(input || '').replace(/\\\\/g, '/');
    const absolute = raw.startsWith('/') ? raw : '/' + raw;
    const parts = [];
    for (const part of absolute.split('/')) {
      if (!part || part === '.') continue;
      if (part === '..') parts.pop();
      else parts.push(part);
    }
    return '/' + parts.join('/');
  }

  function dirname(file) {
    const clean = normalizePath(file);
    const idx = clean.lastIndexOf('/');
    return idx <= 0 ? '/' : clean.slice(0, idx);
  }

  function joinPath() {
    return normalizePath(Array.from(arguments).join('/'));
  }

  function resolvePath() {
    let out = '/';
    for (const part of arguments) {
      if (part == null || part === '') continue;
      const text = String(part);
      out = text.startsWith('/') ? text : out.replace(/\\/$/, '') + '/' + text;
    }
    return normalizePath(out);
  }

  function extname(file) {
    const base = normalizePath(file).split('/').pop() || '';
    const idx = base.lastIndexOf('.');
    return idx > 0 ? base.slice(idx) : '';
  }

  const pathShim = { resolve: resolvePath, join: joinPath, dirname, extname };
  const fsShim = {
    existsSync(file) {
      const clean = normalizePath(file);
      return Object.prototype.hasOwnProperty.call(FILES, clean) || Object.keys(FILES).some(name => dirname(name) === clean);
    },
    readFileSync(file) {
      const clean = normalizePath(file);
      if (!Object.prototype.hasOwnProperty.call(FILES, clean)) throw new Error('BROWSER_FS_MISSING: ' + clean);
      return FILES[clean];
    },
    readdirSync(dir) {
      const clean = normalizePath(dir);
      const prefix = clean === '/' ? '/' : clean + '/';
      const names = new Set();
      for (const file of Object.keys(FILES)) {
        if (!file.startsWith(prefix)) continue;
        const rest = file.slice(prefix.length);
        if (rest && !rest.includes('/')) names.add(rest);
      }
      return Array.from(names).sort();
    },
    statSync(file) {
      const clean = normalizePath(file);
      if (!Object.prototype.hasOwnProperty.call(FILES, clean)) throw new Error('BROWSER_FS_MISSING: ' + clean);
      return { size: FILES[clean].length, mtimeMs: 0 };
    }
  };

  function makeHash() {
    let hash = 2166136261;
    return {
      update(value) {
        const text = String(value == null ? '' : value);
        for (let i = 0; i < text.length; i += 1) {
          hash ^= text.charCodeAt(i);
          hash = Math.imul(hash, 16777619) >>> 0;
        }
        return this;
      },
      digest(format) {
        const hex = hash.toString(16).padStart(8, '0');
        return format === 'hex' ? hex : hex;
      }
    };
  }

  const cryptoShim = { createHash: makeHash };

  function resolveModule(fromId, request) {
    if (request === 'fs') return 'fs';
    if (request === 'path') return 'path';
    if (request === 'crypto') return 'crypto';
    if (!request.startsWith('.')) throw new Error('BROWSER_REQUIRE_UNSUPPORTED: ' + request);
    const base = dirname(fromId);
    const candidate = normalizePath(base + '/' + request);
    const withExt = candidate.endsWith('.cjs') ? candidate : candidate + '.cjs';
    if (Object.prototype.hasOwnProperty.call(MODULES, withExt)) return withExt;
    if (Object.prototype.hasOwnProperty.call(MODULES, candidate)) return candidate;
    throw new Error('BROWSER_MODULE_MISSING: ' + request + ' from ' + fromId);
  }

  function requireModule(id) {
    if (id === 'fs') return fsShim;
    if (id === 'path') return pathShim;
    if (id === 'crypto') return cryptoShim;
    if (CACHE[id]) return CACHE[id].exports;
    if (!Object.prototype.hasOwnProperty.call(MODULES, id)) throw new Error('BROWSER_MODULE_MISSING: ' + id);
    const module = { exports: {} };
    CACHE[id] = module;
    const localRequire = request => requireModule(resolveModule(id, request));
    const fn = new Function('require', 'module', 'exports', '__filename', '__dirname', MODULES[id] + '\\n//# sourceURL=ysbzs-browser:' + id);
    fn(localRequire, module, module.exports, id, dirname(id));
    return module.exports;
  }

  function createBrowserLocalEngine(options) {
    const api = requireModule('/src/adapters/serverAuthorityAdapter.cjs');
    const adapter = api.createServerAuthorityAdapter(Object.assign({ allowDebugCommands: true, includeAuthoritativeState: false }, options || {}));
    return Object.assign({}, adapter, {
      action(command) { return adapter.run(command); },
      view(playerId) { return { ok: true, viewModel: adapter.getViewModel(playerId || 'p1') }; },
      report(mode) { return { ok: true, report: adapter.getTextReport(mode || 'player') }; },
      save(playerId) { return { ok: true, save: adapter.exportSave(playerId || 'p1', { sessionId: 'browser-local' }) }; },
      load(saveDoc, playerId) { return adapter.importSave(saveDoc, playerId || 'p1'); }
    });
  }

  window.__YSBZS_LOCAL_ENGINE_FACTORY__ = createBrowserLocalEngine;
  window.__YSBZS_LOCAL_ENGINE_BUNDLE__ = { generatedBy: 'tools/build_local_engine_bundle.cjs', modules: Object.keys(MODULES).length, files: Object.keys(FILES).length };
})();
`;

fs.writeFileSync(outFile, bundle);
console.log(`built ${path.relative(root, outFile)} (${Buffer.byteLength(bundle)} bytes, ${moduleFiles.length} modules, ${dataFiles.length} data files)`);
